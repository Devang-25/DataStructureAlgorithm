package array;

import java.util.Scanner;

public class GFG {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int t = sc.nextInt();

        for (int i = 0; i < t; i++) {
            int n = sc.nextInt();
            int[] m = new int[n - 1];
            for (int j = 0; j < n - 1; j++) {
                m[j] = sc.nextInt();
            }


        }
    }
}